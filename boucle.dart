void main() {
  // Boucle for : affiche les nombres de 1 à 10
  print("Boucle for :");
  for (int i = 1; i <= 10; i++) {
    print(i);
  }

  // Boucle while : affiche les nombres de 10 à 1
  print("\nBoucle while :");
  int j = 10; // Initialisation
  while (j >= 1) {
    print(j);
    j--; // Décrémentation
  }
}
